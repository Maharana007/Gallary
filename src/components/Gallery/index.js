class Gallery extends Component {
  state = {selectedImageId: 0, imageList: imagesList}

  updateSelectedImage = id => {
    this.setState({selectedImageId: id})
  }

  renderSelectImage = () => {
    const {imageList, selectedImageId} = this.state
    const {imageUrl, imageAltText} = imageList[selectedImageId]
    return <img className="main-img" src={imageUrl} alt={imageAltText} />
  }

  render() {
    const {imageList, selectedImageId} = this.state

    // const filterImages= this.getFilterImages()
    return (
      <div className="container">
        {this.renderSelectImage()}
        <h1 className="title"> Nature Photography</h1>
        <p className="name">Nature Photography by Sankar </p>
        <ul className="thumbnail-card">
          {imageList.map(eachImage => (
            <ThumbnailItem
              key={eachImage.id}
              thumbnailImages={eachImage}
              isActive={selectedImageId === eachImage.id}
              onClickUpdateId={this.updateSelectedImage}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
